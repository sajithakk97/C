/******************************************************************************
Floyd's Triangle

*******************************************************************************/
#include <stdio.h>

int main()
{
   int i,j,n=4,val=1;
   for(i=1;i<=n;i++){
       for(j=1;j<=i;j++){
           printf("%d ",val);
           val++;
       }
       printf("\n");
   }
   return 0;
}
